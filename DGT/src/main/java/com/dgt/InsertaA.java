    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
       String Enunciado = txtEnunciado.getText();
       String r1 = txtR1.getText();
       String r2 = txtR2.getText();
       String r3 = txtR3.getText();
       String rc = txtRC.getText();
       String linea = Enunciado+";"+r1+";"+r2+";"+r3+";"+rc+";0X";
       File fichero = new File("C:\\Archivos\\mediadgt\\traficoA.csv");
        try {
            PrintWriter pw = new PrintWriter(new FileWriter(fichero,true));
            pw.print("\n"+linea);
            pw.close();
            JOptionPane.showMessageDialog(btnInsertar, "Pregunta Insertada");
            super.setVisible(false);
        } catch (IOException ex) {
            Logger.getLogger(InsertaA.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
        
        
    }//GEN-LAST:event_btnInsertarActionPerformed

